const mongoose = require("mongoose");
const { ObjectId } = mongoose.Schema;

//Product display(title, id, color,,price,brand,shipping and images)

const productSchema = new mongoose.Schema(
  {
    title: {
      type: String,
      trim: true,
      required: true,
      maxlength: 32,
      text: true,
    },
  
    // slug is the same as id but it's more precise for finding products
    slug: {
      type: String,
      unique: true,
      lowercase: true,
      index: true,
    },
    description: {
      type: String,
      required: true,
      maxlength: 2000,
      text: true,
    },
    price: {
      type: Number,
      required: true,
      trim: true,
      maxlength: 32,
    },
    category: {
      type: ObjectId,
      ref: "Category",
    },
    subs: [
      {
        type: ObjectId,
        ref: "Sub",
      },
    ],
    quantity: Number,
    sold: {
      type: Number,
      default: 0,
    },
    images: {
      type: Array,
    },
    shipping: {
      type: String,
      enum: ["Yes", "No"],
    },
    color: {
      type: String,
      enum: ["Black", "Brown", "Silver", "White", "Blue", "Grey"],
    },
    brand: {
      type: String,
      enum: ["Nike", "Denim", "Addidas", "Target", "Under Amor"],
    },
  },
  { timestamps: true }
);

module.exports = mongoose.model("Product", productSchema);
